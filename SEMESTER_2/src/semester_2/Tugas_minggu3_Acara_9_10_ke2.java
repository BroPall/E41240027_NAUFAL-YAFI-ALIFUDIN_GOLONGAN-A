package semester_2;

public class Tugas_minggu3_Acara_9_10_ke2 {

    public static void main(String[] args) {
        int i = 1; 

        System.out.println("          Do__While");
        System.out.println("Bilangan kelipatan 2 (1-100)");
        System.out.println("============================");

        do {
            System.out.println(i);
            i *= 2; 
        } while (i <= 100); 
    }
}
